package operators_and_expressions.shorthand_operator;

public class ShortHandOperator {
    public static void main(String[] args) {
        int a=5;
        System.out.println("Value"+a);
        a += 5;
        System.out.println("Value"+a);
        a-=2;
        System.out.println("Value"+a);
        a*=2;
        System.out.println("Value"+a);
        a/=2;
        System.out.println("Value"+a);
    }


}
